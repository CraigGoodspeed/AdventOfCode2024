package nz.co.goodspeed.advent_2024.days.day6;

public class NewMovement {
    int row, column;

    public NewMovement(int row, int column) {
        this.row = row;
        this.column = column;
    }

    public int getRow() {
        return row;
    }

    public void setRow(int row) {
        this.row = row;
    }

    public int getColumn() {
        return column;
    }

    public void setColumn(int column) {
        this.column = column;
    }
}
